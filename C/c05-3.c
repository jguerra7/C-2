#include <stdio.h>
#include <stdlib.h>

int main(void) {


	int a, b;
	printf("a を入力:");
	scanf("%d", &a);
	printf("b を入力:");
	scanf("%d", &b);
	printf("%d + %d = %d\n", a, b, a + b);
	printf("%d - %d = %d\n", a, b, a - b);
	printf("%d * %d = %d\n", a, b, a * b);
	printf("%d / %d = %d\n", a, b, a / b);
	return 0;

}
